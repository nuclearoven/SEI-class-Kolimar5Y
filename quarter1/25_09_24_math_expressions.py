#substract
def substract(a, b):
    """Function subtracts b from a"""
    return a - b
print(substract(3, 5))
#multiply
def multiply(a, b):
    """Function multiplies a and b"""
    return a * b
print(multiply(3, 5))
#divide
def divide(a, b):
    """Function divides a and b"""
    return a / b
print(divide(3, 5))
#floordivide
def floordivide(a, b):
    """Function divides a and b and rounds to the nearest integer"""
    return a // b
print(floordivide(3, 5))
#modulus
def modulus(a, b):
    """Function finds the remainder after dividing a and b"""
    return a % b
print(modulus(3, 5))
#power
def power(a, b):
    """Function calculates a to the power of b"""
    return pow(a, b)
print(power(3, 5))

